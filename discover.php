<h3>Culture</h3><hr>
<div class="grid-container">
    <div class="grid-item1"><p>Himachal Pradesh was one of the few states that had remained largely untouched by external customs, largely due to its difficult terrain. With remarkable economic and social advancements, the state has changed rapidly. Himachal Pradesh is a multireligious, multicultural as well as a multilingual state like other Indian states. Western Pahari languages also known as Himachali languages are widely spoken in the state. Some of the most commonly spoken individual languages are Kangri, Mandeali, Kulvi, Chambeali, Bharmauri and Kinnauri.
The Hindu communities residing in Himachal include the Brahmins, Kayasthas, Rajputs, Sunars, Kannets, Rathis and Kolis. The tribal population of the state consists mainly of Gaddis, Gujjars, Kanauras, Pangwalas, Bhots, Swanglas and Lahaulas.
